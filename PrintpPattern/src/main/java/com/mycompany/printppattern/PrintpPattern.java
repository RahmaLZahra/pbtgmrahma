package com.mycompany.printppattern;

/**
 *
 * @author HP
 */
public class PrintpPattern {

    public static void main(String[] args) {
        // Pola bagian kiri
        System.out.print("  *\n");
        System.out.print(" ***\n");
        System.out.print("******\n");
        System.out.print("*******\n");

        // Spasi antara dua pola
        System.out.print("       ");

        // Pola bagian kanan
        System.out.print("*\n");
        System.out.print(" ***********\n");
        System.out.print("  *********\n");
        System.out.print("   *******\n");
        System.out.print("    *****\n");
        System.out.print("     ***\n");
        System.out.print("      *\n");
    }
}
